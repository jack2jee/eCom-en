    public function login(Request $req) {

        $user = User::where(['email'=>$req->email])->first();
        if(!$user || !Hash::check($req->password, $user->password)) {

            return "Username of Password is not matched";
        } else {
            $req->session()->put('user', $user);
            return redirect('/');
        }


